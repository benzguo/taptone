
extension UIColor {
    class func tt_grayColor() -> UIColor {
        return UIColor(red: 0.2, green: 0.2, blue: 0.2, alpha: 1.0)
    }
    class func tt_orangeColor() -> UIColor {
        return UIColor(red: 0.97, green: 0.49, blue: 0.18, alpha: 1.0)

    }
    class func tt_whiteColor() -> UIColor {
        return UIColor(red: 0.97, green: 0.97, blue: 0.97, alpha: 1.0)
    }
}